DROP TABLE IF EXISTS SPRZATANIE_CZYNNOSCI;
DROP TABLE IF EXISTS UROCZYSTOSCI;
DROP TABLE IF EXISTS JEDZENIE_PRZEPISY;

CREATE TABLE SPRZATANIE_CZYNNOSCI(
  NAZWA VARCHAR(180) PRIMARY KEY NOT NULL COLLATE utf8_polish_ci,
  DATA_OSTATNIEGO_SPRZATANIA DATE,
  DATA_NASTEPNEGO_SPRZATANIA DATE,
  CZESTOTLIWOSC INT(6) NOT NULL
);

CREATE TABLE UROCZYSTOSCI(
  OSOBA VARCHAR(180) PRIMARY KEY NOT NULL COLLATE utf8_polish_ci,
  DATA_UROCZYSTOSCI DATE,
  RODZAJ VARCHAR(50) NOT NULL COLLATE utf8_polish_ci
);

CREATE TABLE JEDZENIE_PRZEPISY(
  NAZWA VARCHAR(180) PRIMARY KEY NOT NULL,
  DATA_OSTATNIEGO_PRZYGOTOWANIA DATE,
  SKLADNIKI VARCHAR(500),
  SPOSOB_PRZYGOTOWANIA VARCHAR(500)
)COLLATE='utf8_polish_ci';